let contentData = document.querySelector(".contentData");

let scholars = document.querySelector("#scholars");

function fillScholars(stuff) {

  let html = "";
  stuff.items.forEach((e) => {
    html += `<span class="contentSubheading">${e.heading}</span>`;
    html += `<table><thead><tr>`;

    e.title.forEach((element) => {
      html += `<th>${element}</th>`;
    });
    html += "</thead></tr></tbody>";

    e.data.forEach((element) => {
      html += `<tr>`;

      for (let key of Object.keys(element)) {
        html += `<td>${element[key]}</td>`;
      }

      html += `</tr>`;
    });
    html += "</tbody></table>";
  });
  return html;
}
